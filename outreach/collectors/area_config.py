def get_search_queries(industry: str, area: str) -> list[str]:
    """
    業種とエリアの組み合わせから検索クエリリストを生成する。

    Args:
        industry: 業種名（例：「歯科医院」）
        area:     エリア名（例：「東京都渋谷区」）

    Returns:
        検索クエリのリスト
    """
    keywords = INDUSTRY_KEYWORDS.get(industry, [industry])
    return [f"{kw} {area}" for kw in keywords]


def get_all_areas() -> list[str]:
    """全エリアのフラットなリストを返す。"""
    result = []
    for pref, cities in AREA_CONFIG.items():
        for city in cities:
            result.append(f"{pref}{city}")
    return result
