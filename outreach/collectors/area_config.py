def get_search_queries(industry: str, area: str) -> list[str]:
    """
    業種とエリアの組み合わせから検索クエリリストを生成する。

    Args:
        industry: 業種名（例：「歯科医院」）
        area:     エリア名（例：「青森県青森市」）

    Returns:
        検索クエリのリスト
    """
    keywords = INDUSTRY_KEYWORDS.get(industry, [industry])
    return [f"{kw} {area}" for kw in keywords]


def get_all_areas() -> list[str]:
    """全エリアのフラットなリストを返す（都道府県+市区町村）。"""
    result = []
    for pref, cities in AREA_CONFIG.items():
        for city in cities:
            result.append(f"{pref}{city}")
    return result


def get_random_area() -> str:
    """ランダムにエリアを1件返す（スケジューラー用）。"""
    import random
    areas = get_all_areas()
    return random.choice(areas)


def get_random_industry() -> str:
    """ランダムに業種を1件返す（スケジューラー用）。"""
    import random
    return random.choice(list(INDUSTRY_KEYWORDS.keys()))
